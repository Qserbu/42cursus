char	*ft_strchr(const char *str, int c)
{
	char	*s;

	s = (char *)str;
	while (s[0] != '\0')
	{
		if (s[0] == (unsigned char)c)
			return (s);
		s++;
	}
	if ((unsigned char)c == '\0')
		return (s);
	return (0);
}
